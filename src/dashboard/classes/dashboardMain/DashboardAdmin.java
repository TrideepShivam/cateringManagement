    private void getPresentStaffs(){
        try{
                SimpleDateFormat ft= new SimpleDateFormat("dd-MM-yyyy");
                String today = ft.format(new java.util.Date());
                DefaultTableModel tableModel = (DefaultTableModel) presentStaffTable.getModel();
                Connection con = databaseConnection();
                String query = "select name,staffs.email,contact,joining from attendance join staffs on staffs.email = attendance.email where date='"+today+"';";
                Statement stmt = con.createStatement();
                ResultSet rs = stmt.executeQuery(query);
                int countPresent=0;
                for(int i=0;i<presentStaffTable.getRowCount();i++){
                    tableModel.removeRow(i);
                }
                while(rs.next()){
                    countPresent++;
                   tableModel.addRow(new Object[]{rs.getString("name"),rs.getString("email"),rs.getString("contact"),rs.getString("joining")});
                }
                lblPresentStaff.setText(Integer.toString(countPresent));
                con.close();
            }catch(Exception e){
                JOptionPane.showMessageDialog(contentContainer, e);
                //con.close();
            }
    }
    
    
    private void getStaffList(){
            try{
                DefaultTableModel tableModel = (DefaultTableModel) staffListTable.getModel();
                Connection con = databaseConnection();
                String query = "select * from Staffs;";
                Statement stmt = con.createStatement();
                ResultSet rs = stmt.executeQuery(query);
                int countStaff=0,countBlocked=0;
                for(int i=0;i<staffListTable.getRowCount();i++){
                    tableModel.removeRow(i);
                }
                while(rs.next()){
                   Boolean status = rs.getBoolean("status");
                   if(!status)countBlocked++;
                   countStaff++;
                   tableModel.addRow(new Object[]{rs.getString("name"),rs.getString("email"),rs.getString("contact"),rs.getString("gender"),rs.getString("address"),rs.getString("joining"),status});
                }
                lblTotalStaffs.setText(Integer.toString(countStaff));
                lblBlocked.setText(Integer.toString(countBlocked));
                con.close();
            }catch(Exception e){
                JOptionPane.showMessageDialog(contentContainer, e);
                //con.close();
            }
    }
    private Connection databaseConnection(){
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            return DriverManager.getConnection("jdbc:mysql://localhost:3306/catering_management","root","");
        }catch(Exception e){
            JOptionPane.showMessageDialog(contentContainer, e);
            return null;
        }
    }
